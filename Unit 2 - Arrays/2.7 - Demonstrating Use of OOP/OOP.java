    private void btnCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculateActionPerformed
        // TODO add your handling code here:
        try{
        inputCar i;
        carBrand = txtBrand.getText();
        carYear = txtYear.getText();
        carColour = txtColour.getText();
        
        // shows object inherits properties, but also creates its own property
        i = new inputCar(carBrand,carYear,carColour);
        
        // checks if object is an instance of class, and checks for inheritance
        if (i instanceof abstractCar){
            carValue = i.calculateValue();
            String output = i.setOutput(carValue);
            
            if ((i.carColour).equals("")){
                txtOutput.setText(output + "\nThe colour of this car was not inputted.");
            } else if (!i.carColour.equals("")){
                txtOutput.setText(output + "\nThe colour of this car is: " + i.carColour);
            }
            
        }
        
        } catch (Exception e){
            txtOutput.setText("There was an error with your inputs.\nOnly type whole numbers for year.");
        }
    }//GEN-LAST:event_btnCalculateActionPerformed

