    // makes an abstract superclass which can be used to show abstraction 
    public abstract class abstractCar {
        // hides data by making variables private - encapsulation
        private String carBrand;
        private String carYear;
        private String carValue;
        
        public abstractCar(String carBrand, String carYear){
            this.carBrand = carBrand;
            this.carYear = carYear;
        }
        public String getBrand(){
            return carBrand;
        }
        public void setBrand(String carBrand){
            this.carBrand = carBrand;
        }
        public String getYear(){
            return carYear;
        }
        public void setYear(String carYear){
            this.carYear = carYear;
        }
        
        // abstract method to be defined when subclass is set
        public abstract String calculateValue();
        
        // output method to show inheritance and polymorphism when subclass is set
        public String setOutput(String carValue){
            return "The car's brand is: " + carBrand + ". The year it was made in is: " + carYear + ".\n" + carValue;
        }
    }
    
    // subclass
    public class inputCar extends abstractCar{
        public String carColour;
        
        // declaration of object inputCar to use abstractCar as superclass, but also take in it's own properties
        public inputCar(String carBrand, String carYear, String carColour) {
            super(carBrand, carYear);
            this.carColour = carColour;
        }
        
        // example of polymorphism - ability to override methods from superclass
        @Override
        public String calculateValue() {
            carValue = "";
            if (Integer.parseInt(carYear) == 2020){
                carValue = "The value of this car is still the same.";
            } else if (Integer.parseInt(carYear)< 2020 && Integer.parseInt(carYear)>2000){
                carValue = "The value of this car has gone down.";
            } else if (Integer.parseInt(carYear) >2020){
                carValue = "This car has not been invented yet/is not real.";
            } else if (Integer.parseInt (carYear) <= 2000){
                carValue = "This car's value has gone down significantly.";
            }
            return carValue;
        
        }
        // subclass has a custom method to show polymorphism
        public void setColour(String carColour){
            this.carColour = carColour;
        }
    }
